    public void print() {
        String dato[] = new String[9];
        clear();
        for (Vehicule i : Login.vehiculesMet.vehiculesList) {
        dato[0] = i.getType();
        dato[1] = i.getBrand();
        dato[2] = i.getTransmition();
        String modelString = Integer.toString(i.getModel());
        dato[3] = modelString;
        
        dato[4] = i.getColor();
        String priceString = Long.toString(i.getPrice());
        dato[5] = "$"+priceString;
        car.addRow(dato);
        }
        
    }

